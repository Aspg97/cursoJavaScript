let a = 5, b = "5";
console.log(a);
console.log(b);

//Operador de igualdad
console.log('a == b -> ',a==b);
console.log(`${a} === ${b} -> ${a==b}`);

//Operador de igualdad exacta
console.log('a === b -> ',a===b);
console.log(`${a} === ${b} -> ${a===b}`);

//Operador distinto
console.log(`${a} != ${b} -> ${a!=b}`);

//Operador distinto exacto
console.log(`${a} != ${b} -> ${a!==b}`);

//Operador menor que
console.log(`${a} < ${b} -> ${a<b}`);

//Operador menor o igual que
console.log(`${a} <= ${b} -> ${a<=b}`);

//Operador mayor que
console.log(`${a} > ${b} -> ${a>b}`);

//Operador mayor o igual que
console.log(`${a} >= ${b} -> ${a>=b}`);